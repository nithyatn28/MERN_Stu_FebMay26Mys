//comparision operators
let x=10; y='10';
console.log("Comparision Operator");
console.log("x==y",x==y);//loose equality
console.log("x===y",x===y);//strict equality
console.log("x!=y",x!=y);//loose inequality
console.log("x!==y",x!==y);//strict inequality
console.log("x>3",x>3);
console.log("x<y",x<y);
console.log("x>=y",x>=y);
console.log("x<=y",x<=y);